import { spawn } from 'child_process';
import path from 'path';
import fs from 'fs';

const DEFAULT_TEST_URLS = [
  'https://youtu.be/lYkR16aXHd8',
  'https://youtu.be/B3P8bQZa3sA',
  'https://youtu.be/WOAavbTcp2A',
  'https://youtu.be/16HMVsXXDV0',
  'https://youtu.be/e5zF0vaSxJg'
];

const testUrls = process.argv[2] ? [process.argv[2]] : DEFAULT_TEST_URLS;
const TEMP_DIR = path.join(process.cwd(), 'downloads', '.test_tmp');

console.log('----------------------------------------------------');
console.log(`🧪 Running Pre-Flight YouTube Download Test (${testUrls.length} URLs)`);
console.log('----------------------------------------------------');

async function testSingleUrl(url, index) {
  const itemDir = path.join(TEMP_DIR, `test_${index}`);
  if (!fs.existsSync(itemDir)) {
    fs.mkdirSync(itemDir, { recursive: true });
  }

  const outputTemplate = path.join(itemDir, '%(title)s.%(ext)s');

  const args = [
    '--js-runtimes', 'node',
    '--skip-download',
    '--no-playlist',
    '--write-thumbnail',
    '--convert-thumbnails', 'jpg',
    '--write-info-json',
    '--write-comments',
    '--extractor-args', 'youtube:player_client=android,web;max-comments=5,5,2,2;comment_sort=top',
    '--output', outputTemplate,
    url
  ];

  console.log(`\n[${index + 1}/${testUrls.length}] Testing URL: ${url}`);

  return new Promise((resolve, reject) => {
    const nodeEnvPath = `${process.env.PATH || ''}:${path.dirname(process.execPath)}`;
    const child = spawn('yt-dlp', args, { env: { ...process.env, PATH: nodeEnvPath } });

    let errorOutput = '';

    child.stderr.on('data', (data) => {
      errorOutput += data.toString();
    });

    child.on('close', (code) => {
      if (code !== 0) {
        return reject(new Error(`yt-dlp failed with exit code ${code}.\n${errorOutput}`));
      }

      try {
        const files = fs.readdirSync(itemDir);
        const infoJsonFile = files.find(f => f.endsWith('.info.json'));
        const thumbFile = files.find(f => f.endsWith('.jpg') || f.endsWith('.png') || f.endsWith('.webp'));

        if (!infoJsonFile) {
          return reject(new Error('No .info.json metadata file generated by yt-dlp'));
        }

        const rawInfo = fs.readFileSync(path.join(itemDir, infoJsonFile), 'utf-8');
        const metadata = JSON.parse(rawInfo);

        console.log(`  ✅ Passed: "${metadata.title}"`);
        console.log(`     👤 Channel: ${metadata.uploader || metadata.channel}`);
        console.log(`     ⏱️  Duration: ${metadata.duration}s | 💬 Comments: ${metadata.comments ? metadata.comments.length : 0}`);
        console.log(`     🖼️  Thumbnail: ${thumbFile || 'N/A'}`);

        resolve();
      } catch (err) {
        reject(err);
      }
    });
  });
}

async function runAllTests() {
  try {
    for (let i = 0; i < testUrls.length; i++) {
      await testSingleUrl(testUrls[i], i);
    }
    console.log('\n----------------------------------------------------');
    console.log(`🎉 ALL ${testUrls.length} YOUTUBE DOWNLOAD TESTS PASSED SUCCESSFULLY!`);
    console.log('----------------------------------------------------');
    fs.rmSync(TEMP_DIR, { recursive: true, force: true });
    process.exit(0);
  } catch (err) {
    console.error('\n----------------------------------------------------');
    console.error('❌ [Test Failed] YouTube extraction error:', err.message);
    console.error('----------------------------------------------------');
    fs.rmSync(TEMP_DIR, { recursive: true, force: true });
    process.exit(1);
  }
}

runAllTests();
